/* ---------------------------------------------------------------------------
 * create a cache
 * --------------------------------------------------------------------------- */
cache_t *cache_create(int pagesize, int pagecount) {
  cache_t* c = malloc(sizeof(cache_t));
  c->pagesize = pagesize;
  c->pagecount = pagecount;
  c->bytes = (cache_t*)malloc(pagesize * pagecount);
  return c;
}

/* ---------------------------------------------------------------------------
 * free a cache
 * --------------------------------------------------------------------------- */
void cache_free(cache_t* c) {
  free(c->bytes);
  free(c);
}

